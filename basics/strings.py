word = "python"
print(word[0])  # p
print(word[-1])  # n
print(word[-6])  # p
print(word[0:2])  # characters from position 0 (included) to 2 (excluded)
print(word[:2])  # character from the beginning position to the pos 2 (excluded)
print(word[3:])  # character from the thirs position to the end
print(word[:])  # all characters
print(word[4:42])  # indexes out of range are handled by the slicing
